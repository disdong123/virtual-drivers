class DrivingDirectionFeignResponseTest {
    private val objectMapper = ObjectMapper().registerModules(KotlinModule.Builder().build(), JavaTimeModule())

    @Test
    fun `네이버 길찾기 api 결과로 DrivingDirectionFeignResponse 인스턴스를 만든다`() {
        val response = objectMapper.readValue(ResponseExample.json, DrivingDirectionFeignResponse::class.java)

        assertInstanceOf(DrivingDirectionFeignResponse::class.java, response)
    }

    @Test
    fun `toDrivingDirectionResponse 메서드는 DrivingDirectionResponse 인스턴스를 만든다`() {
        val response = objectMapper.readValue(ResponseExample.json, DrivingDirectionFeignResponse::class.java)

        assertInstanceOf(DrivingDirectionApiResponse::class.java, response.toDrivingDirectionResponse())
    }
}

